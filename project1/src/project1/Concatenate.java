package project1;

public class Concatenate {

	public static void main(String[] args) {
		String[] Fname = {"Amar","Akbar","Anthony"};
        String[] Lname = {"Singh","Khan","D'Souza"};

        for(int i=0; i<Fname.length; i++) {
        	System.out.println(Fname[i] +" "+Lname[i]);
        }
	}
}
